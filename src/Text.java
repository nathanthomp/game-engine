    public static int[] getGlyph(char character, int scale) {
        int[] glyph = new int[GLYTH_LENGTH * GLYTH_LENGTH];
        if (Character.isDigit(character)) {
            int digit = Character.getNumericValue(character);
            int offset = digit * GLYTH_LENGTH * GLYTH_LENGTH;
            for (int j = 0; j < GLYTH_LENGTH * GLYTH_LENGTH; j++) {
                glyph[j] = NUMBERS[offset + j];
            }
        } else if (Character.isAlphabetic(character)) {
            int charIndex = Character.toUpperCase(character) - 'A';
            int offset = charIndex * GLYTH_LENGTH * GLYTH_LENGTH;
            for (int j = 0; j < GLYTH_LENGTH * GLYTH_LENGTH; j++) {
                glyph[j] = LETTERS[offset + j];
            }
        } else if (Character.isWhitespace(character)) {
            for (int j = 0; j < GLYTH_LENGTH * GLYTH_LENGTH; j++) {
                glyph[j] = 0;
            }
        } else if (character == ':') {
            for (int j = 0; j < GLYTH_LENGTH * GLYTH_LENGTH; j++) {
                glyph[j] = COLON[j];
            }
        }

        if (scale == 1) {
            return glyph;
        }

        return scaleGlyph(glyph, scale);
    }

    private static int[] scaleGlyph(int[] glyph, int scale) {
        int[] scaledGlyth = new int[(GLYTH_LENGTH * scale) * (GLYTH_LENGTH * scale)];
        for (int y = 0; y < GLYTH_LENGTH; y++) {
            for (int x = 0; x < GLYTH_LENGTH; x++) {

                int pixel = glyph[y * GLYTH_LENGTH + x];
                if (pixel == 0) {
                    continue;
                }

                int sx = x * scale;
                int sy = y * scale;

                for (int dy = 0; dy < scale; dy++) {
                    for (int dx = 0; dx < scale; dx++) {
                        scaledGlyth[(sy + dy) * (GLYTH_LENGTH * scale) + (sx + dx)] = 1;
                    }
                }
            }
        }

        return scaledGlyth;
    }
